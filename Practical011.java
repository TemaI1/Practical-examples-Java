// switch

public class Practical011 {
    public static void main(String[] args) {
        int age = 18;
        switch (age) {
            case 23:
                System.out.println("Тебе 23 года");
                break;
            case 18:
                System.out.println("Тебе 18 лет");
                break;
            default:
                System.out.println("Ничего не вышло");
                break;
        }
    }
}
